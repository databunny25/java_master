package library.sys;

public class Book {
	String code;
	String title;
	String author;
	String publisher;
	
	Book(){
		
	}
	
	Book(String code, String title, String author, String publisher){
		this.code = code;
		this.title = title;
		this.author = author;
		this.publisher = publisher;
	}
	
}//end of class
